import sys as io

def rli(): return list(map(int, io.stdin.readline().split()))
def rls(): return list(map(str, io.stdin.readline().split(" ")))
def rlc(): return io.stdin.readline().strip()
def rint(): return int(io.stdin.readline())
def pl(arr): print(' '.join(map(str, arr)))
def pll(arr): print('\n'.join(map(str, arr)))

